# The RANGE FUNCTION returns a "Range Object" of the "Range" datatype
# EXAMPLE 1: If only given 1 value, it produces a range from 0 up to, but not including, the specified value
for i in range(4):
    print(i)
print('\n')
#
# EXAMPLE 2: If given 2 values, it produces a range STARTING AT the 1st value and going up to, but not including, the 2nd value
for i in range(9,13):
    print(i)
print('\n')
#
#Example 3: A 3rd value can be added to specify the "STEP VALUE" (specific about the incrementation)
for i in range(9,23,4):
    print(i)
print('\n')
#
#Example 4: Can also count down instead of up
for i in range(9,-3,-2):
    print(i)
print('\n')
#
#Example 5: Can also count down instead of up
for i in range(9,-3,-2):
    print(i)
print('\n')
